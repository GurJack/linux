#!/bin/bash

# Простейший диалог с сообщением
dialog --title "Пример диалога" --msgbox "Это пример сообщения!" 10 30

# Диалог для ввода текста
dialog --inputbox "Введите ваше имя:" 10 30 2> input.txt
name=$(cat input.txt)
echo "Ваше имя: $name"

# Пример меню
menu_choice=$(dialog --menu "Выберите опцию" 15 40 4 \
    1 "Опция 1" \
    2 "Опция 2" \
    3 "Опция 3" \
    4 "Выход" 2>&1 > /dev/tty)

case $menu_choice in
    1) dialog --msgbox "Вы выбрали Опцию 1!" 10 30 ;;
    2) dialog --msgbox "Вы выбрали Опцию 2!" 10 30 ;;
    3) dialog --msgbox "Вы выбрали Опцию 3!" 10 30 ;;
    *) exit;;
esac

# Пример диалога с подтверждением
dialog --title "Подтверждение" --yesno "Вы уверены?" 7 30

if [ $? = 0 ]; then
    dialog --msgbox "Действие подтверждено!" 10 30
else
    dialog --msgbox "Действие отменено!" 10 30
fi

# Пример чеклиста
dialog --title "Выберите опции" --checklist "Выберите необходимые опции:" 20 50 5 \
    1 "Опция 1" off \
    2 "Опция 2" on \
    3 "Опция 3" off \
    4 "Опция 4" on \
    5 "Опция 5" off 2>&1 > /dev/tty

# Показать результаты
dialog --msgbox "Результаты чеклиста: $?" 10 30
